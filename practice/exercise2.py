# FAULTY CALCULATOR

x=int(input("enter your 1st number "))
y=int(input("enter your 2nd number "))
operator=input("what do you want to calculate ")
if x==4 and y==6 and operator=="+":
    print(20)
elif x==20 and y==5 and operator=="*":
    print(0)
elif operator=="+":
    print(x+y)
elif operator=="-":
    print(x-y)
elif operator=="/":
    print(x/y)
elif operator=="*":
    print(x*y)
else:
    print("enter a valid operator")   


    
       


 
